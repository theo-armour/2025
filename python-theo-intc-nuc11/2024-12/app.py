# Differences between venv and Anaconda:
# 1. venv is a module in Python's standard library for creating lightweight virtual environments.
# 2. Anaconda is a distribution of Python and R for scientific computing and data science, which includes package management and environment management.
# 3. venv is included with Python and does not require a separate installation, while Anaconda needs to be downloaded and installed separately.
# 4. Anaconda comes with a large collection of pre-installed packages and tools, whereas venv creates a clean environment where you install only the packages you need.
# 5. Anaconda includes the conda package manager, which can manage packages and environments for multiple languages, while venv relies on pip for package management.

